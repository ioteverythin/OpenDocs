"""Export the knowledge graph as a Wikipedia-style wiki.

Produces a folder of inter-linked Markdown articles -- one per
community -- plus a navigable ``index.md`` landing page.  Inspired
by Graphify's ``--wiki`` flag.
"""

from __future__ import annotations

import shutil
from datetime import datetime
from pathlib import Path

from ..core.knowledge_graph import KnowledgeGraph
from ..core.models import DocumentModel, GenerationResult, OutputFormat


def generate_wiki(
    doc: DocumentModel,
    kg: KnowledgeGraph,
    output_dir: Path,
) -> GenerationResult:
    """Write a wiki folder to *output_dir* / ``wiki/``.

    Parameters
    ----------
    doc
        Parsed document model (metadata used for the header).
    kg
        Populated knowledge graph **with communities already detected**.
    output_dir
        Root output directory.  A ``wiki/`` sub-folder will be created.

    Returns
    -------
    GenerationResult
    """
    name = doc.metadata.repo_name or "project"
    safe = "".join(c if c.isalnum() or c in "-_ " else "_" for c in name)
    safe = safe.strip().replace(" ", "_")[:80] or "project"
    wiki_dir = output_dir / f"{safe}_wiki"

    try:
        # Ensure a clean wiki directory
        if wiki_dir.exists():
            shutil.rmtree(wiki_dir)
        wiki_dir.mkdir(parents=True, exist_ok=True)

        # Ensure communities exist
        if not kg.communities:
            kg.detect_communities()

        # Generate community articles
        summaries = kg.community_summary()
        article_files: list[Path] = []
        for cs in summaries:
            article_path = _write_community_article(wiki_dir, cs, kg, doc)
            article_files.append(article_path)

        # Generate entity catalog
        _write_entity_catalog(wiki_dir, kg)

        # Generate index
        index_path = _write_index(wiki_dir, doc, kg, summaries)

        return GenerationResult(
            format=OutputFormat.FAQ,  # reuse enum for file result
            output_path=index_path,
        )
    except Exception as exc:
        return GenerationResult(
            format=OutputFormat.FAQ,
            output_path=wiki_dir / "index.md",
            success=False,
            error=str(exc),
        )


